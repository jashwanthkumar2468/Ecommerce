function get_view_cart_products()
{
	$.ajax({
		url:"",
		type:"POST",
		data:{'get':'vcart'},
		success:function(data)
		{
			
			var data = $.parseJSON(data);
			var html = '';
			var htmls = '';
			if(data!='')
			{
				var n = 1;
				$.each(data,function(i){
					var cart = data[i];
					var $n = n++;

					html+= "<tr>";
					html+= "<td data-th='Product'>";
					html+= '<img src='+cart.Image+' width=60%; alt="..." class="img-responsive"/>';
					html+= '<input type="hidden" name="ProductId[]"  class="ProductId" value='+cart.ProductId+'>';
					html+= '</td>';
					html+= '<td><span class="nomargin">'+cart.ProductName+'</span>';      
					html+= '</td>';
					
					html+= '<td data-th="quantity"> '+cart.tagno+'';      
					html+= '</td>';
					html+= '</td>';
					html+= '<td data-th="quantity"> '+cart.TotalPrice+'';
					html+= '</td>';
					html+= '</tr>';
					});
				$('#cart').find('tbody').html(html);
				

				var net_total = 0;
				var net_total_g = 0; 
				$('.qty').each(function(){
					var row = $(this).parent().parent();
						var price  = row.find('.price').val();
						var total = price * $(this).val()-0;
						row.find('.subtotal').val(total);
						row.find('.total').html(total);
						});
						$('.subtotal').each(function(){
						net_total += ($(this).val()-0);

						});
						// net_total_g = (0.18*net_total) + net_total; 
						//$('.net_total').html("<strong>Total Price : Rs." +net_total+"</strong>");
						// $('.net_total_gst').html("<strong>Total Price With GST(18%) : Rs." +net_total_g+"</strong>");
						
						$('.net_totals').val(net_total); 
						$('.grandtotal').val(net_total); 

				$("body").delegate(".qty","keyup",function(event){
					event.preventDefault();
					var row = $(this).parent().parent();
					var price = row.find('.price').val();
					var qty = row.find('.qty').val();
					if (isNaN(qty)) {
						qty = 1;
					};
					if (qty < 1) {
						qty = 1;
					};
					var total = price * qty;
					row.find('.subtotal').val(total);
					row.find('.total').html(total);
					var net_total=0;
					var net_total_g=0; 
					$('.subtotal').each(function(){
						net_total += ($(this).val()-0);
					})
					
					// net_total_g = (0.18*net_total) + net_total; 
					$('.net_total').html("<strong> Total Price : Rs." +net_total+"</strong>");
					// $('.net_total_gst').html("<strong>Total Price With GST(18%) : Rs." +net_total_g+"</strong>");
						
					$('.net_totals').val(net_total);
					$('.grandtotal').val(net_total); 
					

				})

			}
			else
			{
				$('#cart').hide();
				$('.well').hide();
				$('.panel').hide();
				$('.address').hide();
				$('.cod').hide();
				$('.EBS').hide();
				$('.BANK').hide();
				$('.shopping').hide();
				$('hr').css('display','none');
				$('#address_form').hide();
				$('.success').show();
				$('.success').html('<div class="success_order"><div class="order_success"><h1>Your Shopping Cart is empty<img src="<?php echo base_url() ?>images/shopping-cart (1).png"></h1></div></div><div class="continue_shopping"><a class="btn btn-warning" href="<?php echo base_url(); ?>">Continue Shopping</a></div>');
				$('.w3view-cart').html('<i class="fa fa-cart-arrow-down" aria-hidden="true"></i><sup>0</sup>');
			}
		}	
	});


}


